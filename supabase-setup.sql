create or replace function public.current_app_role()
returns text
language sql
stable
security definer
set search_path = public
as $$
  select p.role
  from public.profiles p
  where p.id = auth.uid();
$$;

create or replace function public.module_data_scope(module_name text)
returns text
language sql
stable
security definer
set search_path = public
as $$
  select rp.data_scope
  from public.role_permissions rp
  where rp.role = public.current_app_role()
    and rp.module = module_name;
$$;

create or replace function public.has_module_permission(module_name text, action_name text default 'view')
returns boolean
language plpgsql
stable
security definer
set search_path = public
as $$
declare
  permission_record public.role_permissions%rowtype;
begin
  select *
  into permission_record
  from public.role_permissions rp
  where rp.role = public.current_app_role()
    and rp.module = module_name;

  if not found then
    return false;
  end if;

  case action_name
    when 'view' then return permission_record.can_view;
    when 'create' then return permission_record.can_create;
    when 'update' then return permission_record.can_update;
    when 'delete' then return permission_record.can_delete;
    when 'approve' then return permission_record.can_approve;
    else return false;
  end case;
end;
$$;

create or replace function public.manages_user(target_user_id uuid)
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1
    from public.profiles p
    where p.id = target_user_id
      and p.manager_id = auth.uid()
  );
$$;

create or replace function public.can_access_user(target_user_id uuid, module_name text)
returns boolean
language plpgsql
stable
security definer
set search_path = public
as $$
declare
  data_scope text := public.module_data_scope(module_name);
  actor_role text := public.current_app_role();
begin
  if auth.uid() is null then
    return false;
  end if;

  if module_name = 'wall_of_fame' then
    return true;
  end if;

  if data_scope = 'own' then
    return target_user_id = auth.uid();
  end if;

  if data_scope = 'team' then
    return target_user_id = auth.uid() or public.manages_user(target_user_id);
  end if;

  if data_scope = 'organization' then
    if module_name = 'kudos' then
      return actor_role in ('manager', 'hr', 'admin');
    end if;

    return actor_role in ('hr', 'admin');
  end if;

  if data_scope = 'all' then
    return actor_role = 'admin';
  end if;

  return false;
end;
$$;

create or replace function public.get_my_permissions()
returns table (
  module text,
  can_view boolean,
  can_create boolean,
  can_update boolean,
  can_delete boolean,
  can_approve boolean,
  data_scope text
)
language sql
stable
security definer
set search_path = public
as $$
  select
    rp.module,
    rp.can_view,
    rp.can_create,
    rp.can_update,
    rp.can_delete,
    rp.can_approve,
    rp.data_scope
  from public.role_permissions rp
  where rp.role = public.current_app_role();
$$;

create or replace function public.enforce_leave_status_change()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  if new.status is distinct from old.status or new.approver_id is distinct from old.approver_id then
    if not public.has_module_permission('leave', 'approve') then
      raise exception 'Only authorized approvers can change leave status.';
    end if;

    if old.user_id = auth.uid() then
      raise exception 'You cannot approve or reject your own leave request.';
    end if;

    if not public.can_access_user(old.user_id, 'leave') then
      raise exception 'You cannot approve leave outside your scope.';
    end if;
  end if;

  return new;
end;
$$;

drop trigger if exists leaves_enforce_status_change on public.leaves;
